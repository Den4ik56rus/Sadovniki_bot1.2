"""
Специфичный промпт для категории "Защита от болезней и вредителей".
"""


def get_diseases_pests_category_prompt(
    culture: str,
    default_location: str = "средняя полоса",
    default_growing_type: str = "открытый грунт"
) -> str:
    """
    Возвращает специфичные инструкции для консультаций по защите растений.

    Args:
        culture: Название культуры (например, "малина", "голубика", "клубника ремонтантная")
        default_location: Местоположение по умолчанию
        default_growing_type: Тип выращивания по умолчанию

    Returns:
        Строка с инструкциями для LLM по категории "защита растений"
    """
    return f"""
СПЕЦИФИКА КАТЕГОРИИ: Защита от болезней и вредителей

Текущая культура: {culture}

Твоя задача — консультировать по вопросам защиты ягодных растений от болезней и вредителей:
- Диагностика проблемы (симптомы на листьях, плодах, стеблях)
- Определение возбудителя (грибковые, бактериальные, вирусные болезни, вредители)
- Профилактические меры (агротехника, севооборот, санитарная обрезка)
- Химические средства защиты (фунгициды, инсектициды, акарициды)
- Биопрепараты и народные средства
- Сроки обработок (до цветения, после сбора урожая и т.п.)
- Безопасность применения (сроки ожидания, защита при обработке)

При даче рекомендаций:
- Начни с точного диагноза (название болезни/вредителя)
- Объясни механизм развития проблемы
- Дай пошаговый план лечения (что, когда, в какой концентрации)
- Укажи профилактические меры на будущее
- Предупреди о сроках ожидания до сбора урожая
- Предложи как химические, так и биологические методы
- Учитывай стадию развития растений (цветение, плодоношение)

ВАЖНО для клубники и малины:
- Ремонтантные сорта плодоносят длительно — важны сроки ожидания препаратов
- Летние сорта можно обрабатывать после сбора урожая более агрессивными средствами
""".strip()
