# src/prompts/category_prompts/planting_care.py

"""
Специфичный промпт для категории "Посадка и уход".
"""


def get_planting_care_category_prompt(
    culture: str,
    default_location: str = "средняя полоса",
    default_growing_type: str = "открытый грунт"
) -> str:
    """
    Возвращает специфичные инструкции для консультаций по посадке и уходу.

    Args:
        culture: Название культуры (например, "малина", "голубика", "клубника ремонтантная")
        default_location: Местоположение по умолчанию
        default_growing_type: Тип выращивания по умолчанию

    Returns:
        Строка с инструкциями для LLM по категории "посадка и уход"
    """
    return f"""
СПЕЦИФИКА КАТЕГОРИИ: Посадка и уход

Текущая культура: {culture}

Твоя задача — консультировать по вопросам посадки и ухода за ягодными растениями:
- Сроки посадки (весна/осень, привязка к климату)
- Выбор места (освещение, защита от ветра, севооборот)
- Подготовка почвы (структура, pH, дренаж)
- Схемы посадки (расстояния между растениями и рядами)
- Технология посадки (глубина, расправление корней, полив)
- Регулярный уход (полив, прополка, рыхление, мульчирование)
- Обрезка (формирующая, санитарная, омолаживающая)
- Подготовка к зиме (укрытие, мульчирование, обрезка)

При даче рекомендаций:
- Укажи конкретные сроки с привязкой к климату/региону
- Дай точные схемы посадки (см между растениями)
- Опиши пошагово технологию посадки
- Объясни календарь ухода (что делать в каждый месяц/сезон)
- Предупреди о типичных ошибках (заглубление, загущение посадок и т.п.)
- Учитывай особенности культуры

ВАЖНО для клубники и малины:
- Ремонтантные сорта требуют больше пространства для питания
- Летние сорта имеют свои особенности обрезки и зимовки
""".strip()
