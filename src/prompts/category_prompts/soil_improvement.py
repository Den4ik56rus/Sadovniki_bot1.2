# src/prompts/category_prompts/soil_improvement.py

"""
Специфичный промпт для категории "Улучшение почвы".
"""


def get_soil_improvement_category_prompt(
    culture: str,
    default_location: str = "средняя полоса",
    default_growing_type: str = "открытый грунт"
) -> str:
    """
    Возвращает специфичные инструкции для консультаций по улучшению почвы.

    Args:
        culture: Название культуры (например, "малина", "голубика", "клубника ремонтантная")
        default_location: Местоположение по умолчанию
        default_growing_type: Тип выращивания по умолчанию

    Returns:
        Строка с инструкциями для LLM по категории "улучшение почвы"
    """
    return f"""
СПЕЦИФИКА КАТЕГОРИИ: Улучшение почвы

Текущая культура: {culture}

Твоя задача — консультировать по вопросам улучшения и подготовки почвы для ягодных растений:
- Анализ текущего состояния почвы (тип, структура, pH)
- Требования культуры к почве
- Коррекция pH (известкование, подкисление)
- Улучшение структуры (разрыхление тяжелых почв, удержание влаги в песчаных)
- Повышение плодородия (органика, сидераты, компост)
- Дренаж и водный режим
- Мульчирование (виды мульчи, толщина слоя)
- Долгосрочное планирование (севооборот, восстановление почвы)

При даче рекомендаций:
- Опиши текущее состояние почвы и требования культуры
- Дай пошаговый план улучшения почвы (что вносить, в каком количестве, когда)
- Укажи конкретные материалы и дозировки (торф, песок, компост и т.п.)
- Объясни механизм действия (почему это улучшит почву)
- Предложи долгосрочную стратегию поддержания плодородия
- Учитывай сезонность работ (осенняя/весенняя подготовка)

ВАЖНО для разных культур:
- Голубика требует КИСЛОЙ почвы (pH 4.0-5.0) — это критично!
- Клубника предпочитает слабокислую почву (pH 5.5-6.5)
- Малина толерантна к широкому диапазону pH (5.5-7.0)
- Смородина и крыжовник предпочитают нейтральную или слабокислую почву

ВАЖНО для клубники и малины:
- Ремонтантные сорта сильнее истощают почву — нужно больше органики
- Летние сорта менее требовательны, но тоже нуждаются в плодородной почве
""".strip()
