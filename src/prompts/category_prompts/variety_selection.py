# src/prompts/category_prompts/variety_selection.py

"""
Специфичный промпт для категории "Подбор сортов".
"""


def get_variety_selection_category_prompt(
    culture: str,
    default_location: str = "средняя полоса",
    default_growing_type: str = "открытый грунт"
) -> str:
    """
    Возвращает специфичные инструкции для консультаций по подбору сортов.

    Args:
        culture: Название культуры (например, "малина", "голубика", "клубника ремонтантная")
        default_location: Местоположение по умолчанию
        default_growing_type: Тип выращивания по умолчанию

    Returns:
        Строка с инструкциями для LLM по категории "подбор сортов"
    """
    return f"""
СПЕЦИФИКА КАТЕГОРИИ: Подбор сортов

Текущая культура: {culture}

Твоя задача — консультировать по вопросам подбора сортов ягодных растений:
- Характеристики сортов (сроки созревания, урожайность, вкус, размер ягод)
- Зимостойкость и устойчивость к болезням
- Требования к условиям выращивания
- Назначение (свежее потребление, переработка, заморозка)
- Особенности агротехники конкретных сортов
- Сравнение сортов (плюсы/минусы, для каких целей лучше)
- Районирование (какие сорта подходят для конкретного региона)

При даче рекомендаций:
- Предложи 3-5 конкретных сортов с кратким описанием каждого
- Укажи зону зимостойкости (USDA или российская)
- Опиши ключевые характеристики (вкус, размер, урожайность)
- Отметь особенности агротехники (если есть)
- Сравни сорта между собой по важным для пользователя критериям
- Предупреди о возможных сложностях выращивания
- Дай рекомендацию по лучшему сорту для конкретных условий

ВАЖНО для клубники и малины:
- ОБЯЗАТЕЛЬНО различай ремонтантные и летние сорта — это разные типы с разной агротехникой!
- Ремонтантные (НСД, нейтрального светового дня):
  * Плодоносят всё лето и осень (с июня по заморозки)
  * Требуют усиленного питания
  * Часто выращиваются в однолетней культуре
  * Примеры для клубники: Альбион, Сан Андреас, Монтерей
  * Примеры для малины: Полька, Химбо Топ, Джоан Джей

- Летние (традиционные, июньские):
  * Плодоносят 2-3 недели в июне-июле
  * Менее требовательны к питанию
  * Многолетняя культура
  * Примеры для клубники: Клери, Хоней, Эльсанта
  * Примеры для малины: Лячка, Глен Ампл, Туламин
""".strip()
