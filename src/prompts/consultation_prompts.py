    # 1. –ë–∞–∑–æ–≤—ã–π –ø—Ä–æ–º–ø—Ç (–æ–±—â–∏–π –¥–ª—è –≤—Å–µ—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π)
