    # 2. –ö–∞—Ç–µ–≥–æ—Ä–∏–π–Ω—ã–π –ø—Ä–æ–º–ø—Ç (—Å–ø–µ—Ü–∏—Ñ–∏–∫–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏)
