            "–ü–†–ê–í–ò–õ–ê –ò–°–ü–û–õ–¨–ó–û–í–ê–ù–ò–Ø –ë–ê–ó–´ –ó–ù–ê–ù–ò–ô:\n\n"
            "1. –ü–†–ò–û–†–ò–¢–ï–¢ 1 (Q&A):\n"
            "   - –ï—Å–ª–∏ –¥–∞–Ω –ø–æ–ª–Ω—ã–π –æ—Ç–≤–µ—Ç –Ω–∞ –≤–æ–ø—Ä–æ—Å ‚Äî –∏—Å–ø–æ–ª—å–∑—É–π –î–û–°–õ–û–í–ù–û\n"
            "   - –≠—Ç–æ –ø—Ä–æ–≤–µ—Ä–µ–Ω–Ω—ã–µ –æ—Ç–≤–µ—Ç—ã —Å –≤—ã—Å—à–∏–º –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–æ–º\n\n"
            "2. –ü–†–ò–û–†–ò–¢–ï–¢ 2 (–°–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã):\n"
            "   - –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è —Å–ø–µ—Ü–∏–∞–ª—å–Ω–æ –¥–ª—è —ç—Ç–æ–≥–æ —Ç–∏–ø–∞ –∫—É–ª—å—Ç—É—Ä—ã\n"
            "   - –ò—Å–ø–æ–ª—å–∑—É–π –∫–∞–∫ –æ—Å–Ω–æ–≤–Ω–æ–π –∏—Å—Ç–æ—á–Ω–∏–∫ –ø—Ä–∏ –ø–æ—Å—Ç—Ä–æ–µ–Ω–∏–∏ –æ—Ç–≤–µ—Ç–∞\n\n"
            "3. –ü–†–ò–û–†–ò–¢–ï–¢ 3 (–û–±—â–∏–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã):\n"
            "   - –û–±—â–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –ø–æ –≤—Å–µ–º —Ç–∏–ø–∞–º –∫—É–ª—å—Ç—É—Ä—ã\n"
