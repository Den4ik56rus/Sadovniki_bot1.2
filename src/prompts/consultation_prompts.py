from typing import List, Dict, Any  # –î–ª—è –æ–ø–∏—Å–∞–Ω–∏—è —Ç–∏–ø–æ–≤ –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö


def build_kb_context_snippet(snippets: List[Dict[str, Any]]) -> str:
    """
    –§–æ—Ä–º–∏—Ä—É–µ—Ç —Ç–µ–∫—Å—Ç–æ–≤—ã–π –±–ª–æ–∫ —Å —Ñ—Ä–∞–≥–º–µ–Ω—Ç–∞–º–∏ –∏–∑ –±–∞–∑—ã –∑–Ω–∞–Ω–∏–π –¥–ª—è –≤—Å—Ç–∞–≤–∫–∏ –≤ —Å–∏—Å—Ç–µ–º–Ω—ã–π –ø—Ä–æ–º—Ç.

    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç —Ç—Ä–∏ —É—Ä–æ–≤–Ω—è –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–∞:
    - –£–†–û–í–ï–ù–¨ 1: Q&A –ø–∞—Ä—ã –∏–∑ knowledge_base (–≤—ã—Å—à–∏–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    - –£–†–û–í–ï–ù–¨ 2: –°–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã (—Å—Ä–µ–¥–Ω–∏–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    - –£–†–û–í–ï–ù–¨ 3: –û–±—â–∏–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã (–Ω–∏–∑–∫–∏–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    """
    # –ï—Å–ª–∏ –ø–æ–¥—Ö–æ–¥—è—â–∏—Ö —Ñ—Ä–∞–≥–º–µ–Ω—Ç–æ–≤ –Ω–µ—Ç ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É
    if not snippets:
        return ""

    # –ì—Ä—É–ø–ø–∏—Ä—É–µ–º —Ñ—Ä–∞–≥–º–µ–Ω—Ç—ã –ø–æ —É—Ä–æ–≤–Ω—è–º –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–∞
    level1 = [s for s in snippets if s.get("priority_level") == 1]
    level2 = [s for s in snippets if s.get("priority_level") == 2]
    level3 = [s for s in snippets if s.get("priority_level") == 3]

    lines: List[str] = []

    # –õ–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –ø–æ–ª–Ω—ã—Ö —Ñ—Ä–∞–≥–º–µ–Ω—Ç–æ–≤ –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏
    print(f"\n[KB_CONTEXT] –§–æ—Ä–º–∏—Ä—É–µ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç –∏–∑ {len(snippets)} —Ñ—Ä–∞–≥–º–µ–Ω—Ç–æ–≤")

    # –£–†–û–í–ï–ù–¨ 1: Q&A –ø–∞—Ä—ã (–≤—ã—Å—à–∏–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    if level1:
        lines.append("üìå –ü–†–ò–û–†–ò–¢–ï–¢ 1 - –ü—Ä–æ–≤–µ—Ä–µ–Ω–Ω—ã–µ Q&A –æ—Ç–≤–µ—Ç—ã (–∏—Å–ø–æ–ª—å–∑—É–π –¥–æ—Å–ª–æ–≤–Ω–æ –µ—Å–ª–∏ –¥–∞–Ω –ø–æ–ª–Ω—ã–π –æ—Ç–≤–µ—Ç):")
        for i, snip in enumerate(level1, start=1):
            text = snip.get("content") or snip.get("answer", "")
            lines.append(f"  {i}) {text}")
            print(f"[KB_CONTEXT][–£–†–û–í–ï–ù–¨ 1][#{i}] –î–ª–∏–Ω–∞ —Ç–µ–∫—Å—Ç–∞: {len(text)} —Å–∏–º–≤–æ–ª–æ–≤")
            print(f"[KB_CONTEXT][–£–†–û–í–ï–ù–¨ 1][#{i}] –ü–û–õ–ù–´–ô –¢–ï–ö–°–¢:\n{text}\n{'-'*60}")
        lines.append("")  # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –º–µ–∂–¥—É —É—Ä–æ–≤–Ω—è–º–∏

    # –£–†–û–í–ï–ù–¨ 2: –°–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã (—Å—Ä–µ–¥–Ω–∏–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    if level2:
        lines.append("üìò –ü–†–ò–û–†–ò–¢–ï–¢ 2 - –°–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã –¥–ª—è –¥–∞–Ω–Ω–æ–≥–æ —Ç–∏–ø–∞ –∫—É–ª—å—Ç—É—Ä—ã:")
        for i, snip in enumerate(level2, start=1):
            text = snip.get("content", "")
            lines.append(f"  {i}) {text}")
            print(f"[KB_CONTEXT][–£–†–û–í–ï–ù–¨ 2][#{i}] –î–ª–∏–Ω–∞ —Ç–µ–∫—Å—Ç–∞: {len(text)} —Å–∏–º–≤–æ–ª–æ–≤")
            print(f"[KB_CONTEXT][–£–†–û–í–ï–ù–¨ 2][#{i}] –ü–û–õ–ù–´–ô –¢–ï–ö–°–¢:\n{text}\n{'-'*60}")
        lines.append("")  # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –º–µ–∂–¥—É —É—Ä–æ–≤–Ω—è–º–∏

    # –£–†–û–í–ï–ù–¨ 3: –û–±—â–∏–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã (–Ω–∏–∑–∫–∏–π –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
    if level3:
        lines.append("üìó –ü–†–ò–û–†–ò–¢–ï–¢ 3 - –û–±—â–∏–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã –ø–æ –∫—É–ª—å—Ç—É—Ä–µ:")
        for i, snip in enumerate(level3, start=1):
            text = snip.get("content", "")
            lines.append(f"  {i}) {text}")
            print(f"[KB_CONTEXT][–£–†–û–í–ï–ù–¨ 3][#{i}] –î–ª–∏–Ω–∞ —Ç–µ–∫—Å—Ç–∞: {len(text)} —Å–∏–º–≤–æ–ª–æ–≤")
            print(f"[KB_CONTEXT][–£–†–û–í–ï–ù–¨ 3][#{i}] –ü–û–õ–ù–´–ô –¢–ï–ö–°–¢:\n{text}\n{'-'*60}")

    # –°–∫–ª–µ–∏–≤–∞–µ–º –≤—Å–µ —Å—Ç—Ä–æ–∫–∏
    result = "\n".join(lines)
    print(f"[KB_CONTEXT] –ò—Ç–æ–≥–æ–≤—ã–π –∫–æ–Ω—Ç–µ–∫—Å—Ç: {len(result)} —Å–∏–º–≤–æ–ª–æ–≤\n")
    return result


async def build_terminology_section() -> str:
    """
    –§–æ—Ä–º–∏—Ä—É–µ—Ç —Å–µ–∫—Ü–∏—é —Å–æ —Å–ª–æ–≤–∞—Ä—ë–º —Ç–µ—Ä–º–∏–Ω–æ–ª–æ–≥–∏–∏ –∏–∑ –ë–î.
    """
    from src.services.db.terminology_repo import get_all_terminology

    try:
        terms = await get_all_terminology()
        if not terms:
            return ""

        lines = ["–°–ª–æ–≤–∞—Ä—å —Ç–µ—Ä–º–∏–Ω–æ–≤ (–∏—Å–ø–æ–ª—å–∑—É–π —ç—Ç–∏ —Ñ–æ—Ä–º—É–ª–∏—Ä–æ–≤–∫–∏):"]
        for term in terms:
            lines.append(f"- –í–º–µ—Å—Ç–æ '{term['term']}' –∏—Å–ø–æ–ª—å–∑—É–π '{term['preferred_phrase']}'")

        return "\n".join(lines) + "\n"
    except Exception as e:
        print(f"[build_terminology_section] Error: {e}")
        return ""


